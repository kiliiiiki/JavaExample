package com.yedam.product;

public class Product {
	
	private int productId;
	private String productName;
	private String productSize;
	private int productStock;
	private String productReturn;
	private String productRetreason;
	private String productRefund;
	private String productRefreason;
	private String productMisdel;
	private String productMisreason;
	
	public int getProductId() {
		return productId;
	}
	public void setProductId(int productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductSize() {
		return productSize;
	}
	public void setProductSize(String productSize) {
		this.productSize = productSize;
	}
	public int getProductStock() {
		return productStock;
	}
	public void setProductStock(int productStock) {
		this.productStock = productStock;
	}
	public String getProductReturn() {
		return productReturn;
	}
	public void setProductReturn(String productReturn) {
		this.productReturn = productReturn;
	}
	public String getProductRetreason() {
		return productRetreason;
	}
	public void setProductRetreason(String productRetreason) {
		this.productRetreason = productRetreason;
	}
	public String getProductRefund() {
		return productRefund;
	}
	public void setProductRefund(String productRefund) {
		this.productRefund = productRefund;
	}
	public String getProductRefreason() {
		return productRefreason;
	}
	public void setProductRefreason(String productRefreason) {
		this.productRefreason = productRefreason;
	}
	public String getProductMisdel() {
		return productMisdel;
	}
	public void setProductMisdel(String productMisdel) {
		this.productMisdel = productMisdel;
	}
	public String getProductMisreason() {
		return productMisreason;
	}
	public void setProductMisreason(String productMisreason) {
		this.productMisreason = productMisreason;
	}
	
	
}
